package io.helidon.examples.quickstart.mp.pojo;

public class Pokemon {

    private String id;
    private String name;
    private String type;
    private int arge;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getArge() {
        return arge;
    }

    public void setArge(int arge) {
        this.arge = arge;
    }
}

